package main

import "fmt"

func main() {
    fmt.Println("go" + "lang") // golang
    fmt.Println(len("golang")) // 6
    fmt.Println("1+1 = ", 1+1) // 2
    //9 = 1001
    fmt.Println(9>>2) // shift to right 2
    fmt.Println(true && false) // false
    fmt.Println(true || false) // true
    fmt.Println(!true)  // false
}

