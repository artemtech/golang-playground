package main

import "fmt"

func main() {
        var a = "initial value"
        println(a) // initial value
        var b, c int = 1, 2
        println(b, c) // 1, 2
        var d = true
        println(d) // true
        var e string
        println(e)  // print empty line
        var f int
        println(f) // print 0

        g := 1
        println(g)
        /*
        := is a shorthand declaring and assigning 
        variable in one line without var and data-type
        */
}
